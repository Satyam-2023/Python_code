num1 = 10
num2 = 2

#Arithmetic operators!

print("sum of num1 + num2 =", num1 + num2)
print("sum of num1 - num2 =", num1 - num2)
print("sum of num1 % num2 =", num1 % num2)
print("sum of num1 * num2 =", num1 * num2)
print("sum of num1 / num2 =", num1 / num2)

#Assignment Operators!
a = 4
a += 2
print("a =",a)

b = 5
b **=2
print("b =",b)

